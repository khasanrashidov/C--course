using System;

namespace OOP
{
    public class Points2D
    {
        private int x;
        private int y;

        public Points2D(int x, int y)
        {
            this.x = x;
            this.y = y;
        }

        // "this" keyword is used to refer to the current instance of the class.
        // it is used to distinguish between the fields and the parameters with the same name.
    }
}